import * as React from "react"
import { Link, graphql } from "gatsby"

import Bio from "../components/bio"
import Layout from "../components/layout"
import Seo from "../components/seo"
import { Email } from "../components/email"

const BlogPostTemplate = ({ data, location }) => {
  const post = data.markdownRemark
  const siteTitle = data.site.siteMetadata?.title || `Title`
  const { previous, next } = data
  const externalLink = post.frontmatter.link
  const content = !externalLink ? post.html : post.frontmatter.description

  return (
    <Layout location={location} title={siteTitle}>
      <Seo
        pathname={location.pathname}
        image={post.frontmatter.image.childImageSharp.resize}
        title={post.frontmatter.title}
        description={post.frontmatter.description || post.excerpt}
      />
      <article
        className="blog-post"
        itemScope
        itemType="http://schema.org/Article"
      >
        <header>
          <h1 itemProp="headline">{post.frontmatter.title}</h1>
          <p>{post.frontmatter.date}</p>
        </header>
        <section
          dangerouslySetInnerHTML={{ __html: content }}
          itemProp="articleBody"
        />
        {externalLink ? (
          <div className="link-to-full-article">
            <a href={externalLink}>
              Read full article at {post.frontmatter.site}
            </a>
          </div>
        ) : null}
        <hr />
        <div className="s9-widget-wrapper"></div>
        <footer>
          <Bio />
        </footer>
      </article>
      <nav className="blog-post-nav">
        <ul
          style={{
            display: `flex`,
            flexWrap: `wrap`,
            justifyContent: `space-between`,
            listStyle: `none`,
            padding: 0,
          }}
        >
          <li>
            {previous && (
              <Link to={previous.fields.slug} rel="prev">
                ‚Üê {previous.frontmatter.title}
              </Link>
            )}
          </li>
          <li>
            {next && (
              <Link to={next.fields.slug} rel="next">
                {next.frontmatter.title} ‚Üí
              </Link>
            )}
          </li>
        </ul>
      </nav>
      <div className="contact-list-item">
        <article>
          <header>
            <h2>Subscribe</h2>
          </header>
          <p>
            Get notified whenever I post and get it sent straight to your inbox
          </p>
          <Email />
          <p>
            I promise I won't send spam üòá. You can unsubscribe at any time.
          </p>
        </article>
      </div>
    </Layout>
  )
}

export default BlogPostTemplate

export const pageQuery = graphql`
  query BlogPostBySlug(
    $id: String!
    $previousPostId: String
    $nextPostId: String
  ) {
    site {
      siteMetadata {
        title
      }
    }
    markdownRemark(id: { eq: $id }) {
      id
      excerpt(pruneLength: 160)
      html
      frontmatter {
        title
        date(formatString: "MMMM DD, YYYY")
        description
        link
        site
        image: featured {
          childImageSharp {
            resize(width: 1200) {
              src
              height
              width
            }
          }
        }
      }
    }
    previous: markdownRemark(id: { eq: $previousPostId }) {
      fields {
        slug
      }
      frontmatter {
        title
      }
    }
    next: markdownRemark(id: { eq: $nextPostId }) {
      fields {
        slug
      }
      frontmatter {
        title
      }
    }
  }
`
